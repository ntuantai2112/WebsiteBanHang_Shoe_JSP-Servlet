package com.example.Assignment.model;

public class Sell {

    private Integer id;
    private String  name;


    public Sell() {
    }

    public Sell(Integer id, String name) {
        this.id = id;
        this.name = name;
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
